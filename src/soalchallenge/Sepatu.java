class Sepatu {
    String merk;
    int size;
    String warna;
    String jenis;
    String kategori;
    double harga;
    
    //konstruktor
    public Sepatu (String merk, int size, String warna, String jenis, String kategori, double harga){
        this.merk = merk;
        this.size = size;
        this.warna = warna;
        this.jenis = jenis;
        this.kategori = kategori;
        this.harga = harga;
    }
 }

